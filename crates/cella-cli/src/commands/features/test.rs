    /// When `preserve_test_containers` is `true` the scenario-missing error path
    /// still propagates before any container is started — the backend is never
    /// called and the error message is the same.
    #[tokio::test]
    async fn run_scenarios_missing_script_error_with_preserve() {
        let tmp = tempfile::tempdir().unwrap();
        let project_folder = tmp.path().join("project");
        let feature_id = "feat";
        let test_dir = project_folder.join("test").join(feature_id);
        std::fs::create_dir_all(&test_dir).unwrap();
        std::fs::create_dir_all(project_folder.join("src").join(feature_id)).unwrap();

        let mut scenarios = HashMap::new();
        scenarios.insert("ghost".to_string(), serde_json::json!({}));

        let backend = NeverCalledBackend;
        let flags = RunFlags {
            quiet: true,
            preserve_test_containers: true, // preserve = true: error still propagates
        };
        let result = run_scenarios(
            &backend,
            &project_folder,
            feature_id,
            &scenarios,
            &test_dir,
            None,
            &flags,
        )
        .await;

        assert!(result.is_err());
    }
