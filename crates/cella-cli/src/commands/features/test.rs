    /// global `scenarios.json` has no matching `.sh` file.
    #[tokio::test]
    async fn run_global_scenarios_missing_script_is_error() {
        let tmp = tempfile::tempdir().unwrap();
        let project_folder = tmp.path().join("project");
        let global_dir = tmp.path().join("_global");
        std::fs::create_dir_all(&global_dir).unwrap();
        std::fs::create_dir_all(project_folder.join("src")).unwrap();

        // Write a scenarios.json that names a scenario whose .sh is absent.
        let scenarios_json = global_dir.join("scenarios.json");
        std::fs::write(
            &scenarios_json,
            r#"{"missing_global": {"image": "ubuntu:focal"}}"#,
        )
        .unwrap();

        let backend = NeverCalledBackend;
        let flags = RunFlags {
            quiet: true,
            preserve_test_containers: false,
        };
        let result =
            run_global_scenarios(&backend, &project_folder, &global_dir, None, &flags).await;

        assert!(
            result.is_err(),
            "expected Err for missing global scenario script"
        );
        let msg = result.unwrap_err().to_string();
        assert!(
            msg.contains("missing_global.sh"),
            "error message should name the missing file; got: {msg}"
        );
    }

