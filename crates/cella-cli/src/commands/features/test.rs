/// Run a test script inside the container, streaming output to stdout/stderr.
async fn run_test_script(
    client: &dyn cella_backend::ContainerBackend,
    container_id: &str,
    workspace_folder: &str,
    script_name: &str,
    quiet: bool,
) -> Result<bool, Box<dyn std::error::Error + Send + Sync>> {
    use cella_backend::ExecOptions;

    let script_path = format!("{workspace_folder}/{script_name}");

    if quiet {
        let result = client
            .exec_command(
                container_id,
                &ExecOptions {
                    cmd: vec!["bash".into(), script_path],
                    user: Some("root".to_string()),
                    env: None,
                    working_dir: Some(workspace_folder.to_string()),
                },
            )
            .await
            .map_err(|e| -> Box<dyn std::error::Error + Send + Sync> { Box::new(e) })?;
        Ok(result.exit_code == 0)
    } else {
        let stdout_writer: Box<dyn std::io::Write + Send> = Box::new(std::io::stdout());
        let stderr_writer: Box<dyn std::io::Write + Send> = Box::new(std::io::stderr());
        let result = client
            .exec_stream(
                container_id,
                &ExecOptions {
                    cmd: vec!["bash".into(), script_path],
                    user: Some("root".to_string()),
                    env: None,
                    working_dir: Some(workspace_folder.to_string()),
                },
                stdout_writer,
                stderr_writer,
            )
            .await
            .map_err(|e| -> Box<dyn std::error::Error + Send + Sync> { Box::new(e) })?;
        Ok(result.exit_code == 0)
    }
}

/// Upload all files from a host directory into a container path.
async fn upload_dir(
    client: &dyn cella_backend::ContainerBackend,
    container_id: &str,
    src_dir: &Path,
    container_dest: &str,
) -> Result<(), Box<dyn std::error::Error + Send + Sync>> {
    let mut files = Vec::new();
    collect_files(src_dir, src_dir, container_dest, &mut files)?;
    if !files.is_empty() {
        client
            .upload_files(container_id, &files)
            .await
            .map_err(|e| -> Box<dyn std::error::Error + Send + Sync> { Box::new(e) })?;
    }
    Ok(())
}

/// Recursively collect files from `dir`, building `FileToUpload` entries with
/// paths relative to `base` appended to `container_dest`.
fn collect_files(
    dir: &Path,
    base: &Path,
    container_dest: &str,
    out: &mut Vec<cella_backend::FileToUpload>,
) -> Result<(), Box<dyn std::error::Error + Send + Sync>> {
    for entry in std::fs::read_dir(dir)? {
        let entry = entry?;
        let path = entry.path();
        if path.is_dir() {
            collect_files(&path, base, container_dest, out)?;
        } else {
            let rel = path.strip_prefix(base).unwrap_or(&path);
            let container_path = format!(
                "{container_dest}/{}",
                rel.to_string_lossy().replace('\\', "/")
            );
            let content = std::fs::read(&path)?;
            // Mark shell scripts executable; everything else is read-only.
            let mode = if path
                .extension()
                .and_then(|e| e.to_str())
                .is_some_and(|e| e.eq_ignore_ascii_case("sh"))
            {
                0o755
            } else {
                0o644
            };
            out.push(cella_backend::FileToUpload {
                path: container_path,
                content,
                mode,
            });
        }
    }
    Ok(())
}

/// Recursively copy a directory tree on the host.
fn copy_dir_all(src: &Path, dst: &Path) -> Result<(), Box<dyn std::error::Error + Send + Sync>> {
    std::fs::create_dir_all(dst)?;
    for entry in std::fs::read_dir(src)? {
        let entry = entry?;
        let ty = entry.file_type()?;
        if ty.is_dir() {
            copy_dir_all(&entry.path(), &dst.join(entry.file_name()))?;
        } else {
            std::fs::copy(entry.path(), dst.join(entry.file_name()))?;
        }
    }
    Ok(())
}

/// Remove the temp workspace directory.
fn cleanup_workspace(
    workspace: &TestWorkspace,
) -> Result<(), Box<dyn std::error::Error + Send + Sync>> {
    if workspace.dir.exists() {
        std::fs::remove_dir_all(&workspace.dir)?;
    }
    Ok(())
}

