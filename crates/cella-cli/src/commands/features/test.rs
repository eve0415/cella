/// Temp workspace directory and supporting metadata.
struct TestWorkspace {
    dir: PathBuf,
    dir_name: String,
}

/// Build a temporary workspace for a feature's autogenerated or scenario test.
///
/// Writes a `.devcontainer/devcontainer.json` referencing the local feature
/// source, then copies the feature source directory into `.devcontainer/<id>/`.
fn build_test_workspace(
    project_folder: &Path,
    feature_id: &str,
    base_image: &str,
    extra_features: &[(String, Value)],
) -> Result<TestWorkspace, Box<dyn std::error::Error + Send + Sync>> {
    let timestamp = std::time::SystemTime::now()
        .duration_since(std::time::UNIX_EPOCH)
        .unwrap_or_default()
        .as_millis();
    let dir_name = format!("cella-feat-test-{timestamp}");
    let dir = std::env::temp_dir()
        .join("devcontainercli")
        .join("container-features-test")
        .join(&dir_name);
    let devcontainer_dir = dir.join(".devcontainer");
    std::fs::create_dir_all(&devcontainer_dir)?;

    // Build the features map for devcontainer.json.
    let local_ref = format!("./{feature_id}");
    let mut features_map = serde_json::Map::new();
    features_map.insert(local_ref, Value::Object(serde_json::Map::new()));
    for (k, v) in extra_features {
        features_map.insert(k.clone(), v.clone());
    }

    let config = serde_json::json!({
        "image": base_image,
        "features": features_map,
        "labels": {
            "devcontainer.is_test_run": "true"
        }
    });

    let config_path = devcontainer_dir.join("devcontainer.json");
    std::fs::write(&config_path, serde_json::to_string_pretty(&config)?)?;

    // Copy feature source into .devcontainer/<feature_id>/
    let src = project_folder.join("src").join(feature_id);
    let dst = devcontainer_dir.join(feature_id);
    copy_dir_all(&src, &dst)?;

    Ok(TestWorkspace { dir, dir_name })
}

/// Build a workspace for a global scenario test.
fn build_global_test_workspace(
    project_folder: &Path,
    base_image: &str,
    scenario_config: &Value,
) -> Result<TestWorkspace, Box<dyn std::error::Error + Send + Sync>> {
    let timestamp = std::time::SystemTime::now()
        .duration_since(std::time::UNIX_EPOCH)
        .unwrap_or_default()
        .as_millis();
    let dir_name = format!("cella-feat-test-global-{timestamp}");
    let dir = std::env::temp_dir()
        .join("devcontainercli")
        .join("container-features-test")
        .join(&dir_name);
    let devcontainer_dir = dir.join(".devcontainer");
    std::fs::create_dir_all(&devcontainer_dir)?;

    // The global scenario config IS the devcontainer.json content,
    // but we override the image and add our test label.
    let mut config = scenario_config.clone();
    if let Some(obj) = config.as_object_mut() {
        obj.insert("image".to_string(), Value::String(base_image.to_string()));
        obj.insert(
            "labels".to_string(),
            serde_json::json!({"devcontainer.is_test_run": "true"}),
        );
    }

    // Rewrite local feature references to point to copied sources.
    if let Some(features) = config
        .as_object_mut()
        .and_then(|o| o.get_mut("features"))
        .and_then(|f| f.as_object_mut())
    {
        let keys: Vec<String> = features.keys().cloned().collect();
        for key in keys {
            if let Some(feature_id) = key.strip_prefix("./") {
                let src = project_folder.join("src").join(feature_id);
                if src.is_dir() {
                    let dst = devcontainer_dir.join(feature_id);
                    copy_dir_all(&src, &dst)?;
                }
            }
        }
    }

    let config_path = devcontainer_dir.join("devcontainer.json");
    std::fs::write(&config_path, serde_json::to_string_pretty(&config)?)?;

    Ok(TestWorkspace { dir, dir_name })
}

/// Extract extra features from a scenario config, remapping local `./id`
/// references to the feature source relative path.
fn build_scenario_features(scenario_config: &Value, feature_id: &str) -> Vec<(String, Value)> {
    let Some(features) = scenario_config.get("features").and_then(|f| f.as_object()) else {
        // Default: use the feature itself with empty options.
        return vec![(
            format!("./{feature_id}"),
            Value::Object(serde_json::Map::new()),
        )];
    };
    features
        .iter()
        .map(|(k, v)| (k.clone(), v.clone()))
        .collect()
}

/// Bring up a test container from a temp workspace using the up machinery.
async fn bring_up_test_container(
    client: &dyn cella_backend::ContainerBackend,
    workspace: &TestWorkspace,
    remote_user: Option<&str>,
) -> Result<String, Box<dyn std::error::Error + Send + Sync>> {
    use cella_backend::FileToUpload;
    use cella_config::devcontainer::resolve;

    // Resolve the devcontainer config from the temp workspace.
    let resolved = resolve::config(&workspace.dir, None)?;

    // Build a minimal UpConfig. No daemon, no worktree, no lifecycle secrets.
    let container_name = cella_backend::container_name(&workspace.dir, Some("test"));
    let default_workspace_folder = format!("/workspaces/{}", workspace.dir_name);
    let empty_labels: HashMap<String, String> =
        std::iter::once(("devcontainer.is_test_run".to_string(), "true".to_string())).collect();
    let empty_vec: Vec<String> = Vec::new();
    let empty_mounts: Vec<cella_backend::MountConfig> = Vec::new();
    let up_config = cella_orchestrator::UpConfig {
        resolved: &resolved,
        container_name: &container_name,
        remote_env: &empty_vec,
        cli_remote_env: &empty_vec,
        workspace_folder_from_config: None,
        default_workspace_folder: &default_workspace_folder,
        extra_labels: &empty_labels,
        id_labels: &empty_vec,
        image_strategy: cella_orchestrator::ImageStrategy::Cached,
        remove_existing_container: false,
        skip_checksum: true,
        host_requirement_policy: cella_orchestrator::HostRequirementPolicy::Warn,
        network_rule_policy: cella_orchestrator::NetworkRulePolicy::Skip,
        pull_policy: None,
        build_secrets: Vec::new(),
        extra_networks: Vec::new(),
        mount_flags: cella_orchestrator::MountFlags {
            additional_cli_mounts: &empty_mounts,
            workspace_mount_consistency: None,
            mount_workspace_git_root: false,
            mount_git_worktree_common_dir: false,
        },
        lifecycle_secrets: &empty_vec,
        user_env_probe: cella_env::user_env_probe::UserEnvProbe::LoginInteractiveShell,
        lifecycle_gate: cella_backend::LifecycleGate::default(),
        expect_existing_container: false,
        build_tuning: cella_orchestrator::BuildTuning::default(),
        gpu_availability: cella_backend::GpuAvailability::Detect,
        update_remote_user_uid_default: cella_backend::UpdateRemoteUserUidDefault::On,
        metadata_options: cella_orchestrator::MetadataOptions::default(),
        dotfiles: cella_orchestrator::config::DotfilesConfig::default(),
    };

    let hooks = NullUpHooks;
    // Create a progress sender that discards all events — test containers
    // don't need spinner output wired to a CLI progress renderer.
    let (tx, mut rx) = tokio::sync::mpsc::channel::<cella_orchestrator::ProgressEvent>(256);
    let sink = tokio::spawn(async move { while rx.recv().await.is_some() {} });
    let sender = cella_orchestrator::ProgressSender::new(tx, false);
    let result = cella_orchestrator::up::ensure_up(client, &up_config, &hooks, sender).await;
    let _ = sink.await;
    let result = result.map_err(|e| -> Box<dyn std::error::Error + Send + Sync> { Box::new(e) })?;

    // Apply remoteUser override if given.
    if let Some(user) = remote_user {
        let _ = client
            .exec_command(
                &result.container_id,
                &cella_backend::ExecOptions {
                    cmd: vec![
                        "sh".into(),
                        "-c".into(),
                        format!("id -u {user} > /dev/null 2>&1 || useradd -m {user}"),
                    ],
                    user: Some("root".to_string()),
                    env: None,
                    working_dir: None,
                },
            )
            .await;
    }

    // Upload test-lib to a known scratch location so inject_and_run_test can
    // copy it into the workspace folder later.
    let lib_bytes = TEST_LIB_CONTENT.as_bytes().to_vec();
    client
        .upload_files(
            &result.container_id,
            &[FileToUpload {
                path: "/tmp/dev-container-features-test-lib".to_string(),
                content: lib_bytes,
                mode: 0o755,
            }],
        )
        .await
        .map_err(|e| -> Box<dyn std::error::Error + Send + Sync> { Box::new(e) })?;

    Ok(result.container_id)
}

/// Copy the test directory and test-lib into the workspace folder, then run the
/// named script, returning `true` if it exits with code 0.
async fn inject_and_run_test(
    client: &dyn cella_backend::ContainerBackend,
    container_id: &str,
    project_folder: &Path,
    feature_id: &str,
    workspace_folder: &str,
    script_name: &str,
    quiet: bool,
) -> Result<bool, Box<dyn std::error::Error + Send + Sync>> {
    use cella_backend::ExecOptions;

    let test_dir = project_folder.join("test").join(feature_id);

    // Upload every file from test/<feature_id>/ into the workspace folder.
    upload_dir(client, container_id, &test_dir, workspace_folder).await?;

    // Copy test-lib from /tmp scratch to workspace folder.
    copy_test_lib(client, container_id, workspace_folder).await?;

    // chmod -R 777 on the workspace folder.
    let _ = client
        .exec_command(
            container_id,
            &ExecOptions {
                cmd: vec![
                    "chmod".into(),
                    "-R".into(),
                    "777".into(),
                    workspace_folder.to_string(),
                ],
                user: Some("root".to_string()),
                env: None,
                working_dir: None,
            },
        )
        .await;

    // Execute the test script.
    run_test_script(client, container_id, workspace_folder, script_name, quiet).await
}

